	public ArrayList<String> genRandomData(){
		
		int loopCnt = getRandomNo(3, 50);
		
		ArrayList<String> phonelist = new ArrayList<String>();
		
		for(int i = 0; i<loopCnt; i++ ){
			phonelist.add(getRandomPhoneNo()+"\t"+getRandomPhoneNo()+"\t"+getRandomNo(1,300));
		}
		phonelist.add("123-456-7890"+"\t"+"123-456-7899"+"\t"+getRandomNo(1,300));
		return phonelist;
	}
	
	
	private String getRandomPhoneNo(){
		
		Random random = new Random();
		int randomNumber = random.nextInt(areacodes.length);
		
		int first3Digit = random.nextInt(999);
		int last4Digit = random.nextInt(9999);
		
		return areacodes[randomNumber]+"-"+first3Digit+"-"+last4Digit;
	}
	
	private Integer getRandomNo(int min, int max){
		
		Random random = new Random();
		
		int mins = random.nextInt(max-min)+min;
		
		return mins;
	}
	
	
